#include <stdio.h>
#define maxn 2000

int C[maxn + 1][maxn + 1]; //Массив для хранения биномиальных коэффициентов

int main() {
    int M, T; //Модуль и количество запросов
    scanf("%d %d", &M, &T); //Вводим модуль и количество запросов

    for (int n = 0; n <= maxn; n++) { //Вычисляем все биномиальные коэффициенты до MAX_N
        C[n][0] = C[n][n] = 1;
        for (int k = 1; k < n; k++) { //Вычисляем C(n, k) для 1 <= k < n
            C[n][k] = (C[n - 1][k - 1] + C[n - 1][k]) % M; //Формула Паскаля
        }
    }

    while (T--) { //Обрабатываем каждый запрос
        int N; //Верхний индекс биномиального коэффициента
        long long K; //Нижний индекс биномиального коэффициента
        scanf("%d %lld", &N, &K); //Читаем N и K

        if (K < 0 || K > N) { //Проверяем корректность K
            printf("0\n"); //Если K вне диапазона [0, N], результат 0
        } else {
            printf("%d\n", C[N][(int)K]); //Выводим биномиальный коэффициент C(N, K) по модулю M
        }
    }

    return 0;
}
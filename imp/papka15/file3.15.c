#include <stdio.h>
#include <string.h>
#include <ctype.h>

int main(void) {
    char buf[200]; //Буфер для входной строки
    char token[200]; //Буфер для текущего токена
    int i, pos; //Индексы
    int in_quote; //Флаг нахождения внутри кавычек

    if (!fgets(buf, sizeof(buf), stdin)) { //Читаем строку из stdin
        return 0; //Если не удалось прочитать, выходим
    }

    i = strlen(buf); //Получаем длину строки
    if (i > 0 && buf[i-1] == '\n') buf[i-1] = '\0'; //Убираем символ новой строки

    char *p = buf; //Указатель для перемещения по строке
    while (*p) { //Пока не конец строки
        while (*p && isspace((unsigned char)*p) && !in_quote) p++; //Пропускаем пробелы вне кавычек
        if (!*p) break; //Если дошли до конца строки

        pos = 0; //Сбрасываем позицию в токене
        in_quote = 0; //Сбрасываем флаг кавычек

        if (*p == '"') { //Если встретили открывающую кавычку
            in_quote = 1; //Устанавливаем флаг
            p++; //Пропускаем кавычку
        }

        while (*p) { //Собираем токен
            if (in_quote) { //Если внутри кавычек
                if (*p == '"') { //Если встретили закрывающую кавычку
                    p++; //Пропускаем кавычку
                    in_quote = 0; //Сбрасываем флаг
                    break; //Выходим из цикла сбора токена
                }
            } else { //Если вне кавычек
                if (isspace((unsigned char)*p)) break; //Пробел - конец токена
            }
            if (pos < sizeof(token)-1) { //Если есть место в буфере
                token[pos++] = *p; //Добавляем символ в токен
            }
            p++; //Переходим к следующему символу
        }

        if (pos > 0) { //Если токен не пустой
            token[pos] = '\0'; //Завершаем строку нулем
            printf("[%s]\n", token); //Выводим токен в квадратных скобках
        }
    }

    return 0;
}